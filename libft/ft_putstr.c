#include "libft.h"

void	ft_putstr(char const *s)
{
	int i;

	if (s != NULL)
	{
		i = 0;
		if (s[i] != '\0')
			while (s[i] != '\0')
			{
				write(1, &s[i], 1);
				i++;
			}
	}
}
